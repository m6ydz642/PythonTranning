# 문자열

# 1. 문자열을 만들는 방법 총 4가지
    # 1. 큰 따옴표 "로 양쪽 둘러 싸기
    # 예)    "Hello world"
    # 2. 작은따옴표 '로 양쪽 둘러 싸기
    # 예) 'hello world'

    # 3. 큰따옴표 3개 """를 연속적으로 써서 양쪽을 둘러 싸기
        # 예) """hello"""
print("""hello""")
    # 4. 작은 따옴표 3개 '''를 써서 양쪽으로 둘러 싸기
        # 예) '''hello'''

# 2. 문자열 내부에 작은 따옴표나 큰 따옴표를 기호로 포함 시키고 싶을때
    #문자열에 작은 따옴표 '포함시키기
    # 예)
