    #3. 작은 따옴표 '또는 큰따옴표" 문자열에 포함 시키는 다른 방법은 백 슬래시 \를 사용하면 된다.
    #백슬러시 기호 \를 작은 따옴표 '또는 큰 따옴표- 앞에 삽입하면
    # 백슬러시 기호 \뒤에 작은 따옴표 '나 큰 따옴표"는 문자열을 둘러싸는 기호의 의미가 아니라
    # ' "문자기호 그 자체를 뜻하게 된다.

