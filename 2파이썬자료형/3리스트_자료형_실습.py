        # 파이썬에서는 리스트라는 용어만을 사용한다.
        # 참고2. 아무런 요소도 저장하고 있지 않은 리스트를 반 리스트라고 부르며,
        #   파이썬에서는 []로 표현할 수 있다.
# 리스트 선언 하기
    # 파이썬에서 리스트는 대괄호 []로 감싸서 선언 할 수 있으며,
    # 리스트 안의 요소 들은 쉼표, 로 구분 한다.
    # 문법
    # 리스트 명 (변수명) = [요소1, 요소2, 요소3]

a = [] # 비어있는 리스트 []
b = [1,2,3] # 숫자를 요솟값으로 가지는 리스트 []
c = ['Life', 'is', 'too', 'short'] # 문자열을 요솟값으로 가지는 리스트 []
d = [1,2,'life','is'] # 숫자와 문자열을 함께 요솟값으로 가지는 리스트 []
