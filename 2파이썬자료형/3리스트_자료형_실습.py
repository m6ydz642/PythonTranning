    # 파이썬에서 리스트의 특징

        #1. 리스트에 저장된 요소가 모두 같은 타입일 필요는 없다.
        #2. 리스트에는 요소들이 순서대로 저장되며
        # 각 요소는 0부터 시작하는 인덱스 위치를 사용하여 접근할 수 있다.
